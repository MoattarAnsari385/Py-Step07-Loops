# For Loop With List
l = [1, 2, 4, 5, 6, 7, 3, 9, 8]

for i in l :
    print (i)
print(" ")

# For Loop With Tuple
t = (1, 2, 4, 5, 3, 6, 8, 7)

for i in t :
    print (i)
print(" ")

# For Loop With String
s = "Moattar"

for i in s:
    print (i)
print(" ")

# For Loop With Dictionary
d = {
    "Name": "John",
    "Age": 30,
    "Country": "USA"
}

for i in d:
    print(f"{i} : {d[i]}")