# Table of 5 using While Loop
num = 5  # Start from 5

# The while loop prints the table of 5 from 5 to 50
while (num <= 50):
    print(num) # Print the current value of num
    num += 5 # Increment num by 5 in each iteration

# Table of 5 using For Loop
# The for loop starts from 0 and goes till 50, incrementing by 5 in each step
for i in range(0, 55, 5):
    print(i) # Print the current value of i
