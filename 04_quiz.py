# Write a program to print 1 to 50 using a while loop 

i = 1

while(i <= 50):
    print(i)
    i += 1

# Write a program to print the content of a list using while loops.

mixed_list = [25, "Hello", 3.14, True, None, 42, "Python", 8.9]

i = 0

while(i < len(mixed_list)):
    print(mixed_list[i])
    i += 1